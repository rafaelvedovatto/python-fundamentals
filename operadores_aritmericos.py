# + adição
# - subtração
# / divisão
# // divisão inteira - retornan a parte inteira
# % módulo - retorna o resto da divisão
# * multiplicação
# ** exponenciação

a = 10
b = 20

soma = a + b
print(soma)
print(a+b)

subtracao = a - b
print(subtracao)

multiplicao = a * b
print(multiplicao)

exponenciacao = a ** b
print(exponenciacao)

divisao = a / b
print(divisao)

divisaointeira=a//b
print(divisaointeira)

modulo = a % b
print(modulo)

a += 2 # a = a + 2
print(a)

a *= 2 # a = a * 2
print(a)


